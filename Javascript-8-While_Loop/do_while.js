// hàm do while

//thực thi
do {
	var value = prompt("Nhập số nào đó từ 1 đến 10: ")
	//code sẽ thực hiện hàm do trươc rồi mới xét đến điều kiện dưới while
} while (value < 1 || value > 10) {
	alert("Số bạn vừa nhập là: " + value)
} // xét điều kiên trong while đúng sẽ hiệ ra alert
// nếu sai điều kiện sẽ thực hiện lạ từ "do"

