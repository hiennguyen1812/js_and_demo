// hàm while
 var a = 1; // khai báo a bắt đầu từ 1
 document.write("Bắt đầu vòng lặp </br>") // in ra màn hình "Bắt đầu vòng lặp"

 while(a <= 15) { // bắt đầu vòng lặp với điều kiện a <= 15
	document.write("a sẽ tăng đến: " + a + "<br  />")
	// hiển thị a ra màn hình
	a++
	//a tăng 1 đơn vị liên tục cho đến khi tới điều kiện
 }
 document.write("Đến khi a = 15 sẽ tự dừng vòng lặp")// vì điều kiện a đến 15

